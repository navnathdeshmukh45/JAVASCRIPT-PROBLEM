// How do you remove the middle element from an array

const array =[1,2,3,4,5,6,7];

// middle element index find
const middleindex = Math.floor(array.length / 2)

console.log("middle element index is ",middleindex);

// middle element remove
const middleelement = array[middleindex];
console.log("array element remove",middleelement);

